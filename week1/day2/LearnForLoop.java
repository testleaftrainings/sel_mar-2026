package week1.day2;

public class LearnForLoop {

    public static void main(String[] args) {
        
        //print the number from 1to 10

        for (int i = 1; i <=10; i++) {
            System.out.println(i);
        }
        System.out.println("---------------------");

        for (int i = 10; i >=0; i--) {
            System.out.println(i);
            
        }
    }

}
